// @lc code=start
func moveZeroes(nums []int) {
	// 定义一个慢指针
	slow := 0
	// 定义一个快指针
	for fast := 0; fast < len(nums); fast++ {
		if nums[fast] != 0 {
			nums[slow], nums[fast] = nums[fast], nums[slow]
			slow++
		}
	}
}

